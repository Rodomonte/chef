#define ll long long
#define gc getchar_unlocked
inline ll getl(){
  char c = gc();
  ll n = 0;
  while(c < '0' || c > '9') c = gc();
  while(c >= '0' && c <= '9')
    n = (n<<3) + (n<<1) + c - '0', c = gc();
  return n;
}

#define pc putchar_unlocked
inline void putn(int n){
  if(!n) pc('0');
  char pb[2];
  int pi = 0;
  while(n) pb[pi++] = (n%10) + '0', n /= 10;
  while(pi) pc(pb[--pi]);
}

